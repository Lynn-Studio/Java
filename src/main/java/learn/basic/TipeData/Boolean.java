package learn.basic.TipeData;

// tipe data primitif pada java
// byte     5
// short    5
// int      5
// long     5
// float    5.0
// double   5.0
// boolean  true / false
// char     'A'

public class Boolean{
    public static void main(String[] args){
        // boolean adalah tipe data yang memiliki 2 nilai, yaitu benar dan salah
        // nilai benar direpresentasikan dengan true
        // nilai salah direpresentasikan dengan false

        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);
    }    
}

// Program tersebut diberi nama Boolean dan berada dalam package learn.basic.TipeData.

// Dalam program ini, Anda mendemonstrasikan penggunaan tipe data primitif boolean.

// Tipe data boolean adalah tipe data yang hanya memiliki dua nilai mungkin, yaitu true dan false. Ini digunakan untuk merepresentasikan keadaan atau kondisi logis dalam program.

// Anda mendeklarasikan dua variabel: benar dan salah, yang masing-masing diinisialisasi dengan nilai true dan false.

// Kemudian, Anda menggunakan pernyataan System.out.println() untuk mencetak nilai variabel benar dan salah ke konsol.

// Hasil eksekusi program ini akan mencetak true dan false ke layar, mengonfirmasi bahwa Anda telah berhasil menggunakan tipe data boolean untuk merepresentasikan nilai logis benar dan salah dalam program Java.
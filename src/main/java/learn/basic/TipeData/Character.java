package learn.basic.TipeData;

// tipe data primitif pada java
// byte     5
// short    5
// int      5
// long     5
// float    5.0
// double   5.0
// boolean  true / false
// char     'A'

public class Character{
    public static void main(String[] args){
        // char adalah Data character / huruf
        // membuat char di java menggunakan tanda petik satu diawal dan di akhir
        char A = 'A';
        System.out.println(A); 
    }
}

// 1. Program tersebut diberi nama `Character` dan berada dalam package `learn.basic.TipeData`.

// 2. Dalam program ini, Anda mendemonstrasikan penggunaan tipe data primitif `char`.

// 3. Tipe data `char` digunakan untuk menyimpan karakter atau huruf tunggal. Karakter di Java dikelilingi oleh tanda petik satu (`'`) di awal dan di akhir.

// 4. Anda mendeklarasikan sebuah variabel bernama `A` dengan tipe data `char` dan menginisialisasinya dengan karakter `'A'`.

// 5. Kemudian, Anda menggunakan pernyataan `System.out.println()` untuk mencetak nilai variabel `A` ke konsol.

// Hasil eksekusi program ini akan mencetak karakter 'A' ke layar. Program ini menunjukkan penggunaan tipe data `char` untuk menyimpan dan mencetak karakter tunggal dalam bahasa Java.